/*
 *
 * __m512 ALM_PROTO_OPT(vrs16_sqrtf)(__512)
 *
 *
 * Computes square root of 16 32-bit values packed in a vector
 */

 __m512
ALM_PROTO_ARCH_ZN5(vrs16_sqrtf)(__m512 x)
{
    __m512 result;

    result = _mm512_sqrt_ps(x);

    return result;
}